# Daftar lokasi yang disimulasikan (sesuai dengan skrip deteksi)
LOKASI_LIST = ["Simpang_A", "Simpang_B", "Tol_Km_10"]

lines = ["timestamp,lokasi,hari,is_weekend,jumlah_kendaraan"]
base_date = datetime(2025, 1, 1, 0, 0)

# Simulasi 30 hari untuk setiap lokasi
for day_offset in range(30):
    current_date = base_date + timedelta(days=day_offset)
    hari_ke = current_date.weekday()
    is_weekend = 1 if hari_ke >= 5 else 0
    
    for lokasi in LOKASI_LIST:
        # Karakteristik tiap lokasi berbeda
        if lokasi == "Simpang_A":
            loc_multiplier = 1.0
        elif lokasi == "Simpang_B":
            loc_multiplier = 0.8 # Lebih sepi
        else: # Tol
            loc_multiplier = 1.5 # Lebih ramai
            
        day_variation = random.randint(-5, 5)

        for jam, menit, count in base_data:
            new_dt = current_date.replace(hour=jam, minute=menit)
            variation = random.randint(-3, 3)
            
            # Pola weekend
            multiplier = 0.7 if is_weekend else 1.0
            
            # Hitung jumlah kendaraan
            new_count = int(max(0, (count + day_variation + variation) * multiplier * loc_multiplier))
            
            # Simpan: timestamp, lokasi, hari, is_weekend, jumlah_kendaraan
            lines.append(f"{new_dt.strftime('%Y-%m-%d %H:%M')},{lokasi},{hari_ke},{is_weekend},{new_count}")

output_path = r"D:\KHAFID\diky\aplikasi\traffic-dashboard\data\traffic.csv"
with open(output_path, "w") as f:
    f.write("\n".join(lines))

